uint16_t bcm_lut_crc16_fast(const uint8_t *data, uint32_t len)
{
    uint16_t crc = 0xFFFFU;
    uint32_t i;

    for (i = 0; i < len; i++) {
        uint8_t idx = (uint8_t)((crc ^ data[i]) & 0xFFU);
        crc = (uint16_t)((crc >> 8U) ^ s_crc16Table[idx]);
    }

    return crc;
}

