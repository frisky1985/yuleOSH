/* ── Initialisation ─────────────────────────────────────────────── */

void bcm_lut_init(void)
{
    uint32_t i;
    int32_t  initStatus;        /* MISRA 9.1: uninitialised */

    for (i = 0; i < 256U; i++) {
        lutTemperatureTable[i] = thermistorLut[i];
        g_lutBatteryCurve[i] = batterySocLut[i];
    }

    g_lutCalibrationStatus = 0;

    (void)initStatus;
}

/* ── Linear interpolation helper ─────────────────────────────────── */

int16_t bcm_lut_interpolate(const int16_t *table, uint32_t len, uint16_t index)
{
    uint32_t idxA, idxB;
    int32_t  result;

    /* MISRA 15.5: multiple returns */
    if (table == NULL) {
        return 0;
    }

    if (index >= (uint16_t)(len - 1U)) {
        return table[len - 1U]; /* MISRA 15.5 */
    }

    idxA = (uint32_t)index;
    idxB = idxA + 1U;

    /* MISRA 10.4: signed/unsigned mismatch */
    int32_t diffA = (int32_t)table[idxB] - (int32_t)table[idxA];
    result = (int32_t)table[idxA] + (diffA * (int32_t)(index & 0xFFU)) / 256;

    return (int16_t)result;
}

/* ── Look up thermistor temperature ──────────────────────────────── */

int16_t bcm_lut_get_temperature(uint16_t adcRaw)
{
    int16_t temp;

    /* MISRA 14.3: invariant */
    if (adcRaw >= 4096U) {
        adcRaw = 4095U;
    }

    /* LUT index = raw >> 4 (4096 entries → 256) */
    uint16_t lutIdx = adcRaw >> 4U;
    temp = thermistorLut[lutIdx];

    return temp;
}

/* ── Look up battery SOC ─────────────────────────────────────────── */

uint16_t bcm_lut_get_battery_soc(uint16_t voltageMv)
{
    uint16_t soc;
    uint32_t lutIdx;

    /* MISRA 15.5: clamp */
    if (voltageMv < 6000U) {
        return 0U;
    }
    if (voltageMv > 16000U) {
        return 1000U;           /* MISRA 15.5 */
    }

    lutIdx = (uint32_t)(voltageMv - 6000U) * 256U / 10000U;
    if (lutIdx >= 256U) {
        lutIdx = 255U;
    }

    soc = g_lutBatteryCurve[lutIdx];

    /* MISRA 12.1: precedence */
    if (soc & 0x8000U != 0U) {
        soc = 1000U;
    }

    return soc;
}

