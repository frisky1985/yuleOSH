def generate_stubs(
    arxml_path: str,
    output_dir: str = "generated/stubs",
    swc_filter: str = "",
) -> Dict[str, StubModule]:
    """Generate stubs from an ARXML file.

    This is the entry point called by ``yuleosh autosar gen-stub``.

    Args:
        arxml_path: Path to the ARXML file.
        output_dir: Output directory for generated files.
        swc_filter: Optional SWC name filter (generates only matching SWC).

    Returns:
        Dict mapping SWC short names to their StubModule descriptors.
    """
    from yuleosh.autosar.parser import ARXMLParser

    parser = ARXMLParser()
    swcs = parser.parse_swc(arxml_path)

    if swc_filter:
        swcs = [s for s in swcs if s.short_name == swc_filter]
        if not swcs:
            log.warning("SWC '%s' not found in %s", swc_filter, arxml_path)
            return {}

    gen = StubGenerator(project_dir=output_dir)
    results = gen.generate_all(swcs)

    # Print summary
    print(f"\n  📦 AUTOSAR Stub Generation Summary")
    print(f"  {'=' * 45}")
    for swc_name, module in results.items():
        print(f"  ✅ {swc_name}: {len(module.functions)} stubs")
        print(f"     └─ {module.header_path}")
        print(f"     └─ {module.source_path}")
    print(f"\n  Total SWCs: {len(results)}")
    print(f"  Output:     {Path(output_dir).resolve()}")
    print()

    return results


def register_cli(subparsers) -> None:
    """Register the ``autosar gen-stub`` subcommand on the given subparsers group.

    Designed to be called from the yuleOSH CLI's main parser setup::

        # In yuleosh_cli.py's _build_parser() or main():
        p_autosar = sub.add_parser("autosar", help="AUTOSAR management")
        asub = p_autosar.add_subparsers(dest="autosar_sub")
        from yuleosh.autosar.stubgen import register_cli
        register_cli(asub)
    """
    p_stub = subparsers.add_parser(
        "gen-stub",
        help="Generate RTE stub/mock code from ARXML",
        description="Parse AUTOSAR ARXML and generate C stub/mock files "
                    "for runnable entities and RTE port primitives.",
    )
    p_stub.add_argument(
        "arxml",
        type=str,
        help="Path to the ARXML file",
    )
    p_stub.add_argument(
        "--output", "-o",
        type=str,
        default="generated/stubs",
        help="Output directory (default: generated/stubs)",
    )
    p_stub.add_argument(
        "--swc",
        type=str,
        default="",
        help="Generate stubs for a specific SWC only",
    )
    p_stub.add_argument(
        "--verbose", "-v",
        action="store_true",
        help="Verbose output",
    )
    p_stub.set_defaults(func=_handle_gen_stub_command)


def _handle_gen_stub_command(args) -> None:
    """Handle the ``yuleosh autosar gen-stub`` CLI command."""
    import sys
    from pathlib import Path

    arxml_path = Path(args.arxml)
    if not arxml_path.exists():
        print(f"Error: file not found: {arxml_path}", file=sys.stderr)
        sys.exit(1)

    if args.verbose:
        logging.getLogger("autosar.stubgen").setLevel(logging.DEBUG)

    try:
        generate_stubs(
            arxml_path=str(arxml_path),
            output_dir=args.output,
            swc_filter=args.swc,
        )
    except Exception as e:
        print(f"Error generating stubs: {e}", file=sys.stderr)
        sys.exit(1)
